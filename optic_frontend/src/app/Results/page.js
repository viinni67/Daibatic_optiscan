"use client";

import { useEffect, useState, useRef } from "react";
import { useRouter } from "next/navigation";
import ReactMarkdown from 'react-markdown';
import remarkGfm from 'remark-gfm';
import Page from "../Loader/page";
// import Loader

export default function Home() {
    const router = useRouter();
    const [text, settext] = useState("");
    const [showalert, setshowalert] = useState(false);
    const commentRef = useRef(null);

    useEffect(() => {
        const timer = setTimeout(() => {
            const results = sessionStorage.getItem("api response");
            settext(results); // Update the state to show the response 
        }, 4000);
        return () => clearTimeout(timer);
    }, []);

    const handleCopy = () => {
        if (text) {
            navigator.clipboard.writeText(text)
                .then(() => {
                    setshowalert(true);
                    setTimeout(() => setshowalert(false), 3000);
                })
                .catch((err) => {
                    console.error("Failed to copy text: ", err);
                });
        } else {
            console.error("No text to copy");
        }
    };

    return (
        <div>
            <h1 className="text-center mb-4 text-4xl font-extrabold tracking-tight text-gray-900 dark:text-red-500">
                Medical Report <span className="underline decoration-8 decoration-blue-400 dark:decoration-blue-600">Generated by AI</span>
            </h1>
            <p className="text-center text-lg font-normal text-white lg:text-xl ">
                Your Vision Matters: Diabetic Retinopathy Screening Made Easy
            </p>

            {!text ? ( <Page/>
                // <div className="flex justify-center">
                //     <div role="status">
                //         <svg aria-hidden="true" className="inline w-8 h-8 text-gray-200 animate-spin dark:text-gray-600 fill-blue-600" viewBox="0 0 100 101" fill="none" xmlns="http://www.w3.org/2000/svg">
                //             <path d="M100 50.5908C100 78.2051 77.6142 100.591 50 100.591C22.3858 100.591 0 78.2051 0 50.5908C0 22.9766 22.3858 0.59082 50 0.59082C77.6142 0.59082 100 22.9766 100 50.5908ZM9.08144 50.5908C9.08144 73.1895 27.4013 91.5094 50 91.5094C72.5987 91.5094 90.9186 73.1895 90.9186 50.5908C90.9186 27.9921 72.5987 9.67226 50 9.67226C27.4013 9.67226 9.08144 27.9921 9.08144 50.5908Z" fill="currentColor" />
                //             <path d="M93.9676 39.0409C96.393 38.4038 97.8624 35.9116 97.0079 33.5539C95.2932 28.8227 92.871 24.3692 89.8167 20.348C85.8452 15.1192 80.8826 10.7238 75.2124 7.41289C69.5422 4.10194 63.2754 1.94025 56.7698 1.05124C51.7666 0.367541 46.6976 0.446843 41.7345 1.27873C39.2613 1.69328 37.813 4.19778 38.4501 6.62326C39.0873 9.04874 41.5694 10.4717 44.0505 10.1071C47.8511 9.54855 51.7191 9.52689 55.5402 10.0491C60.8642 10.7766 65.9928 12.5457 70.6331 15.2552C75.2735 17.9648 79.3347 21.5619 82.5849 25.841C84.9175 28.9121 86.7997 32.2913 88.1811 35.8758C89.083 38.2158 91.5421 39.6781 93.9676 39.0409Z" fill="currentFill" />
                //         </svg>
                //         <span className="sr-only">Loading...</span>
                //     </div>
                // </div>
            ) : (
                <div className="mt-6">
                    {showalert && (
                        <div
                        className="fixed left-4 top-1/2 p-2 mb-4 text-sm text-green-800 rounded-lg bg-green-50 dark:bg-gray-800 dark:text-green-400 shadow-lg w-64 z-50"
                        role="alert"
                    >
                        <span className="font-medium"><b>Success! </b></span> <b>Report copied to clipboard.</b>
                    </div>
                    )}
                    <div className="flex justify-center">
                        <div className="w-3/4 mb-4 border rounded-lg bg-gray-50 dark:bg-gray-700 p-6 text-gray-900 dark:text-gray-100">
                            <ReactMarkdown
                                children={text}
                                remarkPlugins={[remarkGfm]}
                                className="markdown-content prose prose-lg dark:prose-dark"
                            />
                        </div>
                    </div>
                    <div className="flex justify-center mt-4">
                        <button   onClick={handleCopy} className="px-4 py-2 text-sm font-medium text-white bg-blue-700 rounded-lg hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-blue-300 dark:focus:ring-blue-800">
                            Copy Report
                        </button>
                    </div>
                </div>
            )}
        </div>
    );
}
